package metroSystem;

/**
 * An enumeration class used to distinguish between languages.
 * <br>
 * Considering the actual needs of life in the Greater Bay Area,
 * we have designed support for English, traditional Chinese, simplified Chinese and other language modes.
 * @see <a href="https://www.elegislation.gov.hk/hk/cap5?xpid=ID_1438403279617_003">Official languages</a>
 * @since Sept. 21, 2021
 * @version 2.0
 */
public enum Language {
    English,
    TraditionalChinese,
    SimplifiedChinese
}
